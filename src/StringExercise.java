public class StringExercise {
    public static void main(String[] args) {
        String song="We don't need no education \nWe don't need no thought control";
        System.out.println(song+"\n");

        String a  = "Check \"this\" out!, \"s out of \"s!\n";
        System.out.println(a);

        String sentence="In windows, the main drive is usually C:\\\n";
        System.out.println(sentence);
        String backSlash=("I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!");
        System.out.println(backSlash);

    }
}
